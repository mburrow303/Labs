function respond(name) {
  if (name == 'darth vader') {
    console.log("Go Away");
  } else {
    console.log(`Hello, ${name}`);
  }
}

respond("darth vader");
console.log("Noooooo! That's impossible!");

respond("lex luthor");
console.log("Be gone, you villain!!!");

respond("luke skywalker");
console.log("Hello Luke Skywalker, you are a wonderful friend!");

